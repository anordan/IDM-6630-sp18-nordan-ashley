	<h2>Wes Anderson's Life</h2>
<p>Wes Anderson is an American film director whose works feature a recurring ensemble of actors, including Luke Wilson, Owen Wilson, Bill Murray and Jason Schwartzman. He is known for quirky, comical movies with flawed characters, with projects ranging from The Royal Tenenbaums and The Life Aquatic With Steve Zissou to Moonrise Kingdom and The Grand Budapest Hotel.</p>

	<h3>Early Life</h3>
<p>Filmmaker Wesley "Wes" Wales Anderson was born on May 1, 1969, in Houston, Texas. His father, Melver Anderson, ran an advertising and public relations company, and his mother, Texas Anne Burroughs, worked in both real estate and archaeology. Anderson grew up with his two brothers Eric and Mel, but their parents divorced when Anderson was eight. While trying to cope with the disintegration of his parent's marriage, Anderson often misbehaved at school.</p> 
	
	<h3>Career Breakthrough</h3>
<p>Mainstream success, though, was not far away. With the release of his third full-length film, The Royal Tenenbaums (again written with Owen Wilson), Anderson gained the combination of critical, box office and Academy notice that had so far eluded him. With an all-star cast that included Gene Hackman, Anjelica Houston, Gwyneth Paltrow, Danny Glover, Bill Murray, Ben Stiller and the increasingly famous Luke and Owen Wilson, Anderson described the film at a 2002 press conference as "...a New York film... about a family of—quote unquote—geniuses, and about their failure and their sort of development of their family..." The film grossed more than $50 million domestically at the box office, received an Oscar nomination for Best Screenplay, and enjoyed near unanimous critical praise.</h3>
 

